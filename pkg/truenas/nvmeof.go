// NVMeoFPortSubsysCreate creates an association between a port and a subsystem.
// This makes the subsystem accessible on that port.
// Updated for TrueNAS SCALE 25.10+: uses nvmet.port_subsys.create API.
// Returns the created association object for tracking purposes.
func (c *Client) NVMeoFPortSubsysCreate(ctx context.Context, portID, subsysID int) (*NVMeoFPortSubsys, error) {
	// Check version support
	if err := c.CheckNVMeoFSupport(ctx); err != nil {
		return nil, err
	}

	params := map[string]interface{}{
		"port_id":   portID,
		"subsys_id": subsysID,
	}

	result, err := c.Call(ctx, "nvmet.port_subsys.create", params)
	if err != nil {
		// TrueNAS SCALE 25.10+ may return "Invalid params" when the association already exists,
		// instead of "already exists". Check if it exists before failing.
		if strings.Contains(err.Error(), "already exists") || strings.Contains(err.Error(), "Invalid params") {
			// Find and return the existing association
			assocs, findErr := c.NVMeoFPortSubsysListBySubsystem(ctx, subsysID)
			if findErr == nil {
				for _, assoc := range assocs {
					if assoc.PortID == portID {
						klog.V(4).Infof("NVMeoFPortSubsysCreate: port-subsystem association already exists (port=%d, subsys=%d, assoc=%d)", portID, subsysID, assoc.ID)
						return assoc, nil
					}
				}
			}
			// If we got "Invalid params" but couldn't find the association, it's a genuine parameter error
			if strings.Contains(err.Error(), "Invalid params") {
				klog.Errorf("NVMeoFPortSubsysCreate: 'Invalid params' error for port=%d, subsys=%d but association not found - genuine parameter error", portID, subsysID)
			}
		}
		return nil, fmt.Errorf("failed to create port-subsystem association: %w", err)
	}

	return parseNVMeoFPortSubsys(result)
}

// parseNVMeoFPortSubsys converts raw API response to NVMeoFPortSubsys.
func parseNVMeoFPortSubsys(data interface{}) (*NVMeoFPortSubsys, error) {
	m, ok := data.(map[string]interface{})
	if !ok {
		return nil, fmt.Errorf("unexpected NVMe-oF port-subsys format")
	}

	assoc := &NVMeoFPortSubsys{}

	if v, ok := m["id"].(float64); ok {
		assoc.ID = int(v)
	}
	if port, ok := m["port"].(map[string]interface{}); ok {
		if id, ok := port["id"].(float64); ok {
			assoc.PortID = int(id)
		}
	}
	if subsys, ok := m["subsys"].(map[string]interface{}); ok {
		if id, ok := subsys["id"].(float64); ok {
			assoc.SubsysID = int(id)
		}
	}

	return assoc, nil
}

// NVMeoFPortSubsysFindBySubsystem checks if a subsystem is already associated with any port.
// Note: TrueNAS 25.10+ API doesn't support filtering by nested fields (subsys.id),
// so we fetch all associations and filter client-side.
func (c *Client) NVMeoFPortSubsysFindBySubsystem(ctx context.Context, subsysID int) (bool, error) {
	assocs, err := c.NVMeoFPortSubsysListBySubsystem(ctx, subsysID)
	if err != nil {
		return false, err
	}
	return len(assocs) > 0, nil
}

