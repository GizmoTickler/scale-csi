// NVMeoFPortList lists all NVMe-oF ports.
func (c *Client) NVMeoFPortList(ctx context.Context) ([]*NVMeoFPort, error) {
	result, err := c.Call(ctx, "nvmet.port.query", []interface{}{}, map[string]interface{}{})
	if err != nil {
		return nil, fmt.Errorf("failed to list NVMe-oF ports: %w", err)
	}

	items, ok := result.([]interface{})
	if !ok {
		return nil, fmt.Errorf("unexpected response format")
	}

	ports := make([]*NVMeoFPort, 0, len(items))
	for _, item := range items {
		port, err := parseNVMeoFPort(item)
		if err != nil {
			continue
		}
		ports = append(ports, port)
	}

	return ports, nil
}

// NVMeoFPortCreate creates a new NVMe-oF port.
// Updated for TrueNAS SCALE 25.10+: uses addr_trtype, addr_traddr, addr_trsvcid.
// Note: addr_adrfam is auto-detected by TrueNAS and should not be passed on create.
func (c *Client) NVMeoFPortCreate(ctx context.Context, transport, address string, port int) (*NVMeoFPort, error) {
	// Check version support
	if err := c.CheckNVMeoFSupport(ctx); err != nil {
		return nil, err
	}

	params := map[string]interface{}{
		"addr_trtype":  strings.ToUpper(transport),
		"addr_traddr":  address,
		"addr_trsvcid": port,
	}

	result, err := c.Call(ctx, "nvmet.port.create", params)
	if err != nil {
		// TrueNAS SCALE 25.10+ may return "Invalid params" when a port with the same
		// address already exists, instead of "already exists". Check if it exists before failing.
		if IsAlreadyExistsError(err) || strings.Contains(strings.ToLower(err.Error()), "invalid params") {
			existing, findErr := c.NVMeoFPortFindByAddress(ctx, transport, address, port)
			if findErr == nil && existing != nil {
				klog.V(4).Infof("NVMeoFPortCreate: port %s:%s:%d already exists (ID %d), returning existing", transport, address, port, existing.ID)
				return existing, nil
			}
			// If we got "Invalid params" but the port doesn't exist, it's a genuine parameter error
			if strings.Contains(strings.ToLower(err.Error()), "invalid params") {
				klog.Errorf("NVMeoFPortCreate: 'Invalid params' error for port %s:%s:%d but port not found - genuine parameter error", transport, address, port)
			}
		}
		return nil, fmt.Errorf("failed to create NVMe-oF port: %w", err)
	}

	return parseNVMeoFPort(result)
}

